package examples;

public class CountDown {
    public static void main(String[] args){

     int limit=5;
     for (int i =1; i<=limit;++i){
         System.out.println(i);
     }
     System.out.println("__________________");
     for (int j = 5; j >0;--j ){
         System.out.println(j);
     }
    }
}
